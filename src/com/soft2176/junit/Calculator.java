package com.soft2176.junit;

public class Calculator {
    /**
     * 加法
     * @param a 整数
     * @param b 整数
     * @return 两数之和
     */
    public int add(int a,int b){
    return (a+b);
}

    /**
     * 减法
     * @param a 减数
     * @param b 被减数
     * @return 两数之差
     */
    public int sub(int a,int b){
        return (a-b);
    }
}
